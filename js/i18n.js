  let _currentLang = 'en';

  function detectLanguage() {
    // 1. Check saved preference
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved && SUPPORTED.includes(saved)) return saved;
    } catch (e) {}

    // 2. Check URL param ?lang=xx
    const params = new URLSearchParams(window.location.search);
    const paramLang = params.get('lang');
    if (paramLang && SUPPORTED.includes(paramLang)) return paramLang;

    // 3. Check browser language
    const browserLangs = navigator.languages || [navigator.language || 'en'];
    for (const lang of browserLangs) {
      const code = lang.split('-')[0].toLowerCase();
      if (SUPPORTED.includes(code)) return code;
    }

    return 'en';
  }

  function init() {
    _currentLang = detectLanguage();
    applyTranslations();
    updateHtmlLang();
    updateHtmlDir();
  }

  function setLanguage(lang) {
    if (!SUPPORTED.includes(lang)) return;
    _currentLang = lang;
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    applyTranslations();
    updateHtmlLang();
    updateHtmlDir();

    // Update URL param
    const url = new URL(window.location.href);
    if (lang === 'en') {
      url.searchParams.delete('lang');
    } else {
      url.searchParams.set('lang', lang);
    }
    window.history.replaceState(null, '', url.toString());

    // Dispatch event so other modules can react
    document.dispatchEvent(new CustomEvent('cw:lang-change', { detail: { lang } }));
  }

  function t(key) {
    const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS.en;
    return dict[key] || TRANSLATIONS.en[key] || key;
  }

  function getLang() { return _currentLang; }
  function getSupportedLanguages() { return SUPPORTED; }
  function getLangName(code) { return LANG_NAMES[code] || code; }

  function updateHtmlLang() {
    document.documentElement.setAttribute('lang', _currentLang);
  }

  function updateHtmlDir() {
    document.documentElement.setAttribute('dir', _currentLang === 'ar' ? 'rtl' : 'ltr');
  }

  function applyTranslations() {
    // Apply to all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });

    // Apply to elements with data-i18n-aria
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });

    // Apply to elements with data-i18n-title
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
    });
  }

